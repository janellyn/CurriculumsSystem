create database EducationalPlans;

use EducationalPlans

-- Creating the Faculty/Institute table
CREATE TABLE Faculty_Institute (
    ID INT PRIMARY KEY,
    Name NVARCHAR(255)
);

-- Creating the Speciality table
CREATE TABLE Speciality (
    ID INT PRIMARY KEY,
    Name NVARCHAR(255),
    Code NVARCHAR(50),
    Faculty_Institute INT,
    FOREIGN KEY (Faculty_Institute) REFERENCES Faculty_Institute(ID)
);

-- Creating the Educational Level table
CREATE TABLE Educational_Level (
    ID INT PRIMARY KEY,
    Name NVARCHAR(255)
);

-- Creating the Educational Program table
CREATE TABLE Educational_Program (
    ID INT PRIMARY KEY,
    Name NVARCHAR(255),
    Speciality INT,
    FOREIGN KEY (Speciality) REFERENCES Speciality(ID)
);

-- Creating the Learning Form table
CREATE TABLE Learning_Form (
    ID INT PRIMARY KEY,
    Name NVARCHAR(255)
);

-- Creating the Curriculum table
CREATE TABLE Curriculum (
    ID INT PRIMARY KEY,
    Name NVARCHAR(255),
    Education_Level INT,
    Learning_Form INT,
    Faculty_Institute INT,
    Speciality INT,
    Educational_Program INT,
    FOREIGN KEY (Education_Level) REFERENCES Educational_Level(ID),
    FOREIGN KEY (Learning_Form) REFERENCES Learning_Form(ID),
    FOREIGN KEY (Faculty_Institute) REFERENCES Faculty_Institute(ID),
    FOREIGN KEY (Speciality) REFERENCES Speciality(ID),
    FOREIGN KEY (Educational_Program) REFERENCES Educational_Program(ID)
);

-- Creating the Course table
CREATE TABLE Course (
    ID INT PRIMARY KEY,
    Name NVARCHAR(255),
    Department_Name NVARCHAR(255),
    Disciplines INT,
    Lectures INT,
    Practical INT,
    Laboratory INT,
    Self_Study INT
);

-- Creating the Role table
CREATE TABLE Role (
    ID INT PRIMARY KEY,
    Name NVARCHAR(255)
);

-- Creating the Cabinet table
CREATE TABLE Cabinet (
    ID INT PRIMARY KEY
);

-- Creating the User table
CREATE TABLE Userr (
    ID INT PRIMARY KEY,
    Name NVARCHAR(255),
    Login NVARCHAR(255),
    Password NVARCHAR(255),
    Role INT,
    Cabinet INT,
    FOREIGN KEY (Role) REFERENCES Role(ID),
    FOREIGN KEY (Cabinet) REFERENCES Cabinet(ID)
);

-- Creating the Cabinet-Curriculum relation table
CREATE TABLE Cabinet_Curriculum (
    Cabinet_ID INT,
    Curriculum_ID INT,
    PRIMARY KEY (Cabinet_ID, Curriculum_ID),
    FOREIGN KEY (Cabinet_ID) REFERENCES Cabinet(ID),
    FOREIGN KEY (Curriculum_ID) REFERENCES Curriculum(ID)
);

-- Creating the Course-Curriculum relation table
CREATE TABLE Course_Curriculum (
    Course_ID INT,
    Curriculum_ID INT,
    PRIMARY KEY (Course_ID, Curriculum_ID),
    FOREIGN KEY (Course_ID) REFERENCES Course(ID),
    FOREIGN KEY (Curriculum_ID) REFERENCES Curriculum(ID)
);

